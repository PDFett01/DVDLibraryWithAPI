CREATE PROCEDURE [dbo].[AddDVD](
			@Title VARCHAR(50),
			@ReleaseDate VARCHAR(4),
			@Director VARCHAR(20),
			@Rating VARCHAR(4),
			@Notes VARCHAR(100)	)
AS
BEGIN
	SET NOCOUNT ON;
INSERT INTO DVD (Title, ReleaseDate, Director, Rating, Notes)
VALUES (@Title, @ReleaseDate, @Director, @Rating, @Notes);
END

